// arithmetic  operator
// plus (+)  minus (-)  multiply (*)  divide (/)  modulus (%)  exponent (^)  floor division (//)  increment (++), decrement (--)

var a = 10
var b =3

// addition 
console.log(a+b);
// subtraction
console.log(a-b);
// multiplication
console.log(a*b);
// division
console.log(a/b);
// modulus
console.log(a%b);
// pre increment
console.log(++a);
// post increment
console.log(a++);
// pre decrement
console.log(--b);
// post decrement
console.log(b--);
